a, b = input("Enter two number: ").split(" ")
a = int(a)
b = int(b)
# + is used for addition
print(f"The sum of {a} and {b} is {a + b}")
# - is used for subtraction
print(f"The difference of {a} and {b} is {a - b}")
# * is used for multiplication
print(f"The product of {a} and {b} is {a * b}")
# / is used for division and it returns float value
print(f"The division of {a} and {b} is {a / b}")
# // is used for floor division and it returns integer value
print(f"The floor division of {a} and {b} is {a // b}")
# % is used for modulus and it returns remainder
print(f"The modulus of {a} and {b} is {a % b}")
# ** is used for exponentiation
print(f"The exponentiation of {a} and {b} is {a ** b}")
